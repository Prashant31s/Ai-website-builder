'use client'
import { Button } from '@/components/ui/button'
import { UserDetailContext } from '@/context/UserDetailContext'
import { SignInButton, useAuth, useUser } from '@clerk/nextjs'
import axios from 'axios'
import { ArrowUp, HomeIcon, ImagePlus, Key, LayoutDashboard, Loader2Icon, User } from 'lucide-react'
import Link from 'next/link'
import { useRouter } from 'next/navigation'
import { Router } from 'next/router'
import React, { useContext, useState } from 'react'
import { toast } from 'sonner'
import { v4 as uuidv4} from 'uuid'

const suggestions = [
  {
    label: 'Dashboard',
    prompt: 'Create an analytics dashboard to track customers and revenue data for a SaaS',
    icon: LayoutDashboard
  },
  {
    label: 'SignUp Form',
    prompt: 'Create a modern sign up form with email/password fields, Google and Github login options, and terms checkbox',
    icon: Key
  },
  {
    label: 'Hero',
    prompt: 'Create a modern header and centered hero section for a productivity SaaS. Include a badge for feature announcement, a title with a subtle gradient effect, subtitle, CTA, small social proof and an image.',
    icon: HomeIcon
  },
  {
    label: 'User Profile Card',
    prompt: 'Create a modern user profile card component for a social media website',
    icon: User
  }
]


function Hero() {
    const [userInput, setUserInput] = useState(''); 
    const { user } = useUser()
    const router = useRouter();
    const [loading, setLoading]  = useState(false)
    const {has} = useAuth();
    const {userDetail, setUserDetail} = useContext(UserDetailContext)
    const hasUnlimitedAccess = has && has({ plan: 'unlimited' })

    const createNewProject = async() => {

        if(!hasUnlimitedAccess && userDetail?.credits !<=0 ){
            toast.error('You have no credits left. Please upgrade your Plan')
            return
        }

        const projectId = uuidv4();
        const frameId = Math.floor(Math.random() * 10000);
        setLoading(true)
        const messages = [
            {
                role:"user",
                content: userInput
            }
        ]
        try {
            const result = await axios.post('/api/projects',{
                projectId: projectId,
                frameId: frameId,
                messages: messages,
                credits: userDetail?.credits
            })
            console.log("result", result.data)
            toast.success('Project created ! ')
            //Navigate to playground
            router.push(`/playground/${projectId}?frameId=${frameId}`)
            setUserDetail((prev:any)=>({
                ...prev,
                credits:prev?.credits!-1
            }))
            setLoading(false)
        }
        catch(e){
            toast.error('Internal Server Error')
            console.log(e)
            setLoading(false)
        }
    }

    return (
        <div className='flex flex-col items-center h-[80vh] justify-center'>
            
            {/* header and description */}
            <h2 className='font-bold text-6xl'>What should we design ?</h2>
            <p className='mt-2 text-2xl text-gray-400'>Generate, Edit and Explore design with AI, export code as well</p>

            {/* input box */}
            <div className='w-full max-w-2xl p-5 border mt-5 rounded-2xl'>
                <textarea placeholder='Describe your page design'
                    className='w-full h-24 focus:outline-none focus:ring-0 resize-none'
                    value={userInput}
                    onChange={(event)=>setUserInput(event.target.value)}
                />
                <div className='flex justify-between items-center'>
                    <Button variant={'ghost'} >
                        <ImagePlus/>
                    </Button>
                    {
                        !user ?<SignInButton mode='modal' forceRedirectUrl={'/workspace'}>
                            <Button disabled ={!userInput}>
                                <ArrowUp/>
                            </Button>
                        </SignInButton> :
                        
                        <Button disabled ={!userInput || loading} onClick={createNewProject}>{loading ? <Loader2Icon className='animate-spin'/>: <ArrowUp/>}</Button>
                        
                      
                    }
                </div>
            
          
               
            </div>

            {/* suggestions */}
            <div className='flex mt-4 gap-3'>
                {suggestions.map((suggestion, index)=>(
                    <Button key={index} variant={'outline'} onClick={()=>setUserInput(suggestion.prompt)}> 

                        <suggestion.icon/>
                        {suggestion.label}
                    </Button>
                ))}
            </div>
        </div>
    )
}

export default Hero